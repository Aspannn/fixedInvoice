package pojo.cdata;

public class PublicOffice {
    private String payPurpose;
    private String bik;
    private String iik;

    public PublicOffice() {
    }

    public String getPayPurpose() {
        return payPurpose;
    }

    public String getBik() {
        return bik;
    }

    public String getIik() {
        return iik;
    }

    //Setter

    public void setPayPurpose(String payPurpose) {
        this.payPurpose = payPurpose;
    }

    public void setBik(String bik) {
        this.bik = bik;
    }

    public void setIik(String iik) {
        this.iik = iik;
    }
}
